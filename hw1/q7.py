# Find the remainder
# Time Limit: 0.2s   Memory Limit: 32M
 
# Description
# Given two integers a and b, find the remainder of the equation a/b. 

# Input
# Two integers a and b. 

# Output
# The remainder of a/b. 

# Sample Input
# 5 2

# Sample Output
# 1
a,b = input().split()
print(int(a) % int(b))