# Integer division
# Time Limit: 0.2s   Memory Limit: 32M
 
# Description
# Given two integer inputs a and b, output the quotient of a / b. Leave the remainder behind and only output the result of the division.

# Input
# Two integers.

# Output
# An integer.

# Sample Input
# 5 2

# Sample Output
# 2
a,b = input().split()
print(int(a) // int(b))